/**
 * 検索クエリを正規化する。
 *
 * やること:
 * - trim
 * - Unicode NFKC（全角英数字→半角、半角カタカナ→全角カタカナ）
 * - 全角カタカナ→ひらがな
 *
 * やらないこと:
 * - 意味推測
 * - ローマ字変換
 * - 表記揺れのLLM補完
 */
export function normalizeQuery(input: string): string {
  let s = input.trim();

  // NFKC: 半角カタカナ→全角カタカナ、全角英数字→半角
  s = s.normalize("NFKC");

  // 全角カタカナ→ひらがな
  // 全角カタカナ（標準）: U+30A1–U+30F6
  // ひらがな（対応）:     U+3041–U+3096
  // 差: 0x60 (96)
  // 長音(ーU+30FC)・中点(・U+30FB)・繰り返し(ヽU+30FD ヾU+30FE)は変換しない
  //
  // NOTE: オフセット0x60は清音・濁音・半濁音すべてで正しい。
  //   検証済み: カ(30AB)→か(304B), ガ(30AC)→が(304C), パ(30D1)→ぱ(3071)
  //   特に ヴ(U+30F4)→ゔ(U+3094) は正のマッピング。
  //   「ヴ→う(U+3046)」は誤り。U+3094はひらがな「ゔ」であり、U+3046「う」ではない。
  //   もし範囲を拡張する場合は `codepointAt(0).toString(16)` で必ず実測してから追加すること。
  // 順序: trim → NFKC（半角カタカナ→全角カタカナ）→ 全角カタカナ→ひらがな
  s = s.replace(/[\u30a1-\u30f6]/g, (ch) =>
    String.fromCodePoint(ch.codePointAt(0)! - 0x60),
  );

  return s;
}
